using System.Linq;
using Content.Client.UserInterface.Controls;
using Content.Shared._Arcane.CCVars;
using Content.Shared._Arcane.InfinityDorm;
using Robust.Client.AutoGenerated;
using Robust.Client.UserInterface.Controls;
using Robust.Client.UserInterface.XAML;
using Robust.Shared.Configuration;
using Robust.Shared.Prototypes;
using static Robust.Client.UserInterface.Controls.BaseButton;
using static Robust.Client.UserInterface.Controls.LineEdit;

namespace Content.Client._Arcane.InfinityDorm;

[GenerateTypedNameReferences]
public sealed partial class InfinityDormTeleporterWindow : FancyWindow
{
    [Dependency] private readonly IPrototypeManager _proto = default!;
    [Dependency] private readonly IConfigurationManager _cfg = default!;

    public string SelectedRoom = string.Empty;
    public int SelectedNumber = 0;

    public Action? ConfirmPressed;

    public InfinityDormTeleporterWindow()
    {
        RobustXamlLoader.Load(this);
        IoCManager.InjectDependencies(this);

        ReloadRooms();
        _proto.PrototypesReloaded += OnProtoReload;

        NumberInput.OnTextChanged += args => ValidateDormNumber(args.Text);

        Confirm.OnPressed += _ => ConfirmPressed?.Invoke();

        SetRoomsAvailable(0);
    }

    public override void Close()
    {
        base.Close();

        _proto.PrototypesReloaded -= OnProtoReload;
    }

    public void SetRoomsAvailable(int available)
    {
        RoomsAvailable.Text = $"{available}/{_cfg.GetCVar(ACCVars.MaxUserInfinityDorms)}";
    }

    private void OnProtoReload(PrototypesReloadedEventArgs args)
    {
        if (!args.WasModified<InfinityDormPrototype>() && !args.WasModified<InfinityDormCategoryPrototype>())
            return;

        ReloadRooms();
    }

    private void ReloadRooms()
    {
        if (Disposed)
            return;

        RoomsContainer.RemoveAllChildren();

        var rooms = _proto.EnumeratePrototypes<InfinityDormPrototype>();
        var categories = _proto.EnumeratePrototypes<InfinityDormCategoryPrototype>();

        foreach (var category in categories)
        {
            foreach (var room in rooms.Where(r => r.Category.Id == category.ID))
            {
                var roomButton = new Button()
                {
                    Name = room.ID,
                    Text = Loc.GetString("infinity-dorm-name", ("category", category.Name), ("name", room.ID)),
                    ToolTip = Loc.GetString("infinity-dorm-desc", ("desc", room.Description), ("author", room.Author)),
                    ModulateSelfOverride = category.Color,
                    Margin = new Thickness(0, 0, 0, 3)
                };
                roomButton.OnPressed += args => OnPressedRoomButton(args);

                RoomsContainer.AddChild(roomButton);
            }
        }
    }

    private void OnPressedRoomButton(ButtonEventArgs args)
    {
        if (args.Button.Name is null || !_proto.TryIndex<InfinityDormPrototype>(args.Button.Name, out var prototype))
            return;

        SelectedRoom = prototype.ID;
        CurrentRoom.Text = prototype.Title;
    }

    private void ValidateDormNumber(string text)
    {
        if (!int.TryParse(text, out var newNumber))
            return;

        NumberInput.Text = newNumber.ToString();
        SelectedNumber = newNumber;
    }
}
